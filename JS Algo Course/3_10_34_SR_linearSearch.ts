function linearSearch(arr, el) {
  if (arr == null) return -1;
  if (arr.lenght === 0) return -1;
  if (el == null) return -1;
  for (let i in arr) {
    let val = arr[i];
    if (val === el) return +i;
  }
  return -1;
} // O(n^2)

